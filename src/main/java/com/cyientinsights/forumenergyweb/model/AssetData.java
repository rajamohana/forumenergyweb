package com.cyientinsights.forumenergyweb.model;

public class AssetData {
	
private Object totalAssets;
	
	private Object good;
	
	private Object stable;
	
	private Object critical;

	public Object getTotalAssets() {
		return totalAssets;
	}

	public void setTotalAssets(Object totalAssets) {
		this.totalAssets = totalAssets;
	}

	public Object getGood() {
		return good;
	}

	public void setGood(Object good) {
		this.good = good;
	}

	public Object getStable() {
		return stable;
	}

	public void setStable(Object stable) {
		this.stable = stable;
	}

	public Object getCritical() {
		return critical;
	}

	public void setCritical(Object critical) {
		this.critical = critical;
	}
	

}
