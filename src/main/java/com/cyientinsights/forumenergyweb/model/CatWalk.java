package com.cyientinsights.forumenergyweb.model;

public class CatWalk {
	private Integer totalAssets;
	private Integer good;
	private Integer stable;
	private Integer critical;
	

	public Integer getTotalAssets() {
	return totalAssets;
	}

	public void setTotalAssets(Integer totalAssets) {
	this.totalAssets = totalAssets;
	}

	public Integer getGood() {
	return good;
	}

	public void setGood(Integer good) {
	this.good = good;
	}

	public Integer getStable() {
	return stable;
	}

	public void setStable(Integer stable) {
	this.stable = stable;
	}

	public Integer getCritical() {
	return critical;
	}

	public void setCritical(Integer critical) {
	this.critical = critical;
	}




}
